package com.company;

public class Main {

    public static void main(String[] args) {
	int result = 1 + 2; // 1 + 2 = 3
        System.out.println(result);
        result += 2;// 3 + 2 =5
        System.out.println(result);
        result -= 1; //5-1=4
        System.out.println(result);
        result *= 2; // 4 * 2 =8
        System.out.println(result);
        result /= 8; // 8 / 8 =1
        System.out.println(result);

    }
}
